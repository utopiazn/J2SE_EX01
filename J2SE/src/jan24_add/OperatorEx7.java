package jan24_add;

public class OperatorEx7 {

	public static void main(String[] args){
		boolean power = false;
		System.out.println(power);
		
		power = !power;   // power의 값이 false 에서 true 로 변환
		System.out.println(power);
		
		power = !power; // power의 값이 true 에서 false로 변환
		System.out.println(power); 
		
	}
}

