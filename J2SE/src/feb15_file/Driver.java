package feb15_file;

public class Driver {
	private String driverName;
	public Driver(String driverName){
		this.driverName = driverName;
	}
	public void setDriverName(String driverName){
		this.driverName = driverName;
	}
	public String getDriverName(){
		return driverName;
	}
	public String toString(){
		return driverName;
	}
}
