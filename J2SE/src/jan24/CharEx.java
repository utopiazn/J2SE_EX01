package jan24;

public class CharEx {

	public static void main(String[] args){
		
		char c = 'A'; // ASCII 코드변호 65 = 'A' : 유니코드 0~65535

		char c2 = 'a';
	
		
		
		char c3 = '\n';  // /n /t 값은 값이 들어감.		
		
		

		System.out.println("c의 값음 " + c);		
		
		System.out.println("c2의 값음 " + c2);
		

		System.out.println("c3의 값음 " + c3 + "!");
	}
	
	
}
