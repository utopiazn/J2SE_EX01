package jan24;

public class OperEx4 {
	public static void main(String[] args){
		int a =10;
		int b =20;
		
		boolean c = a<b; // 변수 a가 변수 b보다 작다
		System.out.println("a < b :" +c);
		
		c = (a <= b);  
		System.out.println("a <= b :" +c);
		
		c = (a > b);
		System.out.println("a > b :" +c);
		
		c = (a >= b);
		System.out.println("a >=b :" +c);
		
		c = ( a == b);
		System.out.println("a == b" +c);
		
		c= (a != b);  // 변수 a 와 비느 같지 않다.
		System.out.println("a !=b" +c);		
		
				
		
		
		
	}

}
