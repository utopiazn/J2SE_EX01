public class FlowEx18 {

	public static void main(String[] args){
		
		for(int i=0; i <3;i++){
			
			for(int j=1; j<=3; j++){
				
				for(int k =1 ; k <=3 ; k++){
					
					System.out.println(""+j+j+k);
				}
				
				
				System.out.println();
				
			}
			
		}
		
	}
}
